public class AssignmentOperators {
    public static void main(String args[]) {
        // assignment operators
        int a = 10;

        System.out.println(a);

        a += 10;
        System.out.println(a);

        a -= 5;
        System.out.println(a);

        a *= 2;
        System.out.println(a);

        a /= 2;
        System.out.println(a);

        a %= 2;
        System.out.println(a);
    }
}