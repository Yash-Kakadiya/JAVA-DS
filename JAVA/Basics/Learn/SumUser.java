import java.util.Scanner;

public class SumUser {
    public static void main(String args[]) {
        Scanner sc = new Scanner(System.in);
        // sum of a and b (Input from User);
        int a, b, sum;
        a = sc.nextInt();
        b = sc.nextInt();
        sum = a + b;
        System.out.println("----------------------");
        System.out.println(sum);

        sc.close();
    }
}