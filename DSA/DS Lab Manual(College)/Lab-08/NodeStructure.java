/*
 * 56. Write a program to implement a node structure for singly linked list. Read the data in a node, print the node. 
 */

public class NodeStructure {

    // Node blueprint/structure
    public class Node {
        int data;
        Node next;

        public Node(int data) {
            this.data = data;
            this.next = null;
        }
    }

}
